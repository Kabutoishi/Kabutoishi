package jp.co.f1.intro.ch2;

public class IfElseIfStatement1 {

	public static void main(String[] args) {
		
		//比較用の値を管理するための変数
		int a = 1;
		
		System.out.println("-----if 文判定開始-----");
		
		if(a == 1) {
			System.out.println("変数 a と 1  等しい。");
			
		}else if(a == 2) {
			System.out.println("変数 a と 2 は等しい。");
			
		}else {
			System.out.println("変数 a は 1でも2でもない。");
	
			
		System.out.println("-----if  文の判定終了-----");
		
		
		}

	}

}
